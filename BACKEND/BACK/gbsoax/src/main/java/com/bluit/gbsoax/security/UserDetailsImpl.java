package com.bluit.gbsoax.security;

import java.util.Collection;
import java.util.Collections;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.bluit.gbsoax.models.LoginModel;

public class UserDetailsImpl implements UserDetails {

    private final LoginModel loginModel;

    public UserDetailsImpl(LoginModel loginModel) {
        this.loginModel = loginModel;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return Collections.singleton(new SimpleGrantedAuthority("USER"));
    }

    @Override
    public String getPassword() {
        return loginModel.getPassword();
    }

    @Override
    public String getUsername() {
        return loginModel.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }

    public String getNombre() {
        return loginModel.getUsuarioModel().getNombre();
    }

}
